  static Map<String, Color> _getEmotionColorsMap() {
    Map<String, Color> emotionColor = {};

    for (MapEntry placeHolderEmotion in placeholderEmotions.entries) {
      int? position = placeHolderPosition[placeHolderEmotion.key];
      Color color = position == null
          ? Colors.transparent
          : _getPlaceHolderColor(position);
      emotionColor[placeHolderEmotion.value] = color;
    }
    return emotionColor;
  }

  static Map<String, Color> _getPlaceHolderColorsMap() {
    Map<String, Color> placeHolderColor = {};

    for (MapEntry placeHolderPosition in placeHolderPosition.entries) {
      int position = placeHolderPosition.value;
      Color color = _getPlaceHolderColor(position);
      placeHolderColor[placeHolderPosition.key] = color;
    }
    return placeHolderColor;
  }

  static Color _getPlaceHolderColor(int position) {
    int colorGroup = (position - 1) ~/ 4;
    if (colorGroup >= InteractionConstants.progressiveColors.length) {
      return Colors.transparent;
    }
    return InteractionConstants.progressiveColors[colorGroup];
  }

